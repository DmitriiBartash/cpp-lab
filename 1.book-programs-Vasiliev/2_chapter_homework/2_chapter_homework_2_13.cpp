/* 
Напишите программу для нахождения (в пределах первой сотни) таких целочисленных параметров a, b и c, что выполняется равенство 
a^2 + b^2 = c^2 (например, 3^2 + 4^2 = 5^2). Для вычисления квадратного корня может использоваться функция sqrt()
(доступна после подключения заголовка <cmath>)
*/
#include <iostream>
#include <cmath>

int main() {
    // Изменение кодировки консоли:
    system("chcp 65001>null");

    // Проходим все возможные значения a и b от 1 до 100
    for (int a = 1; a <= 100; ++a) {
        for (int b = a; b <= 100; ++b) { // b начинается с a, чтобы не учитывать перестановки
            int c_squared = a * a + b * b;
            int c = static_cast<int>(sqrt(c_squared)); // Находим c

            // Проверяем, является ли c целым числом и находится ли в пределах 1-100
            if (c * c == c_squared && c <= 100) {
                std::cout << "a = " << a << ", b = " << b << ", c = " << c << std::endl;
            }
        }
    }

    return 0;
}
